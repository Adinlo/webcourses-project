			<p><u>Article 1 : Préambule</u></p>

			<p> Dans le cadre de l'exploitation de son site internet, le site met en œuvre et exploite des traitements
			de données à caractère personnel appartenant aux utilisateurs du site.</p>

			<p>La présente politique a pour objet de satisfaire à l'obligation d'information mise à la charge de l’ensemble et ainsi de formaliser les droits et les obligations des candidats et contacts au regard du traitement de leurs données à caractère personnel.</p>

			<p>Cette politique de confidentialité s'applique au site : http://www.ensemblesaintemarie.fr/.

			<p>La présente politique ne porte que sur les traitements dont l’ensemble est responsable.

			<p>La présente politique de confidentialité a pour but d'exposer aux visiteurs du site :

				<ul>

					<li>La manière dont sont collectées et traitées leurs données à caractère personnel. Doivent être considérées comme données personnelles toutes les données étant susceptibles d'identifier un utilisateur. Il s'agit notamment de son prénom et son nom, son adresse postale,son adresse mail, sa localisation ou encore son adresse IP ;</li>
					<li>Quels sont les droits des visiteurs concernant ces données ;</li>
					<li>Qui est responsable du traitement des données à caractère personnel
					collectées et traitées ;</li>
					<li>A qui ces données sont transmises ;</li>
					<li>La politique du site en matière de fichiers "cookies".</li>
					<p>Cette politique de confidentialité complète les mentions légales.</p>

				</ul>

				<p><u>Article 2 : collecte et traitement de données à caractère personnel</u></p>

			<p>Les données à caractère personnel collectées sur le site https://www.ensemblesaintemarie.fr/ sont les suivantes :</p>

				<ul>

					<li>Les informations transmises par l'utilisateur dans le formulaire d'inscription à la newsletter Groupe (adresse mail) permettant l'envoi de mails d'informations sur l'actualité du Groupe,</li>
					<li>Les caractéristiques techniques des moyens de connexion au site web
					(navigateurs, appareil, adresse IP...) permettant l'amélioration de nos services,</li>
					<li>Et les informations relatives à l'usage du site : IP, temps passés, pages
					consultées, ... utilisées à des fins statistiques (service Google Analytics)</li>

				</ul>

				<p><u>Article 3 : Finalités du traitement</u></p>

			<p>Les données Personnelles collectées au cours de votre navigation sur le Site servent à :</p>

				<ul>

					<li>Permettre le bon fonctionnement du Site Internet et à faciliter votre navigation sur celui-ci ;</li>
					<li>Vous permettre de nous adresser une demande d'informations ;</li>
					<li>Vous permettre de vous inscrire à notre newsletter ;
					De même, nous utilisons des cookies de mesure d'audience afin de connaître le nombre de visites effectuées sur le Site Internet, le nombre de pages vues, ainsi que la fréquence de retours des Visiteurs de notre Site.
					Ainsi, lorsque vous naviguez sur le Site Internet, le dépôt et la lecture de cookies permettent d'analyser votre navigation et de mesurer l'audience du Site Internet.
					Vous avez la possibilité de gérer les cookies utilisés sur notre Site en paramétrant votre poste de travail ou votre navigateur, comme détaillé ci-après (cf. charte des cookies). Les données sont conservées une année.</li>

				</ul>

				<p><u>Article 4 : bases légales</u></p>

			<p>Les traitements reposent sur le consentement des utilisateurs du site, exprimé au moment du
			recueil des données personnelles les concernant, ainsi que notre intérêt légitime.
			Enfin, le recueil des données personnelles des personnes prenant contact avec l’ensemble sainte marie est
			essentiel pour pouvoir apporter une réponse.</p>

				<p><u>Article 5 : transmission des données à des tiers</u></p>

			<p>Partage avec les autorités</p>

			<p>Nous pouvons être amené à divulguer vos données à caractère personnel aux autorités administrative ou judiciaire lorsque leur divulgation est nécessaire à l'identification, l'interpellation ou la
			poursuite en justice de tout individu susceptible de porter préjudice à nos droits, de tout autre
			utilisateur ou d'un tiers. Nous pouvons enfin être légalement tenu de divulguer vos données à
			caractère personnel et ne pouvons dans ce cas nous y opposer.</p>

				<p><u>Article 6 : droit de l’utilisateur</u></p>

			<p>Présentation des droit de l’utilisateur</p>

			<p>Conformément à la réglementation applicable aux Données Personnelles, vous disposez des droits
			suivants :</p>

